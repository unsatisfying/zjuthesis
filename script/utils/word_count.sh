#/bin/bash

if ! [ -f build/zjuthesis.fls ]; then
    echo "Cannot find file 'build/zjuthesis.fls', which is generated by latexmk"
    echo "Please use 'latexmk' command to compile zjuthesis first, then run"
    echo "this script under the project root dir to count words"
    exit 1
fi

cat build/zjuthesis.fls                 \
    | uniq                            \
    | grep body                       \
    | grep .tex                       \
    | grep -v thanksto.tex            \
    | grep -v acknowledgement.tex     \
    | grep -v review.tex              \
    | grep -v original.tex            \
    | grep -v cv.tex                  \
    | cut -d ' ' -f 2                 \
    | awk '{ print "\\input{"$1"}" }' \
    > build/zjuthesis.wordcnt           \
;

texcount build/zjuthesis.wordcnt -inc
